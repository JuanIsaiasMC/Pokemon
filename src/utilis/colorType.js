const colorTypes = (type) => {
    if (type === 'grass') {
        return 'bgr-grass'
    } else if (type === 'ground') {
        return 'bgr-ground'
    } else if (type === 'normal') {
        return 'bgr-normal'
    } else if (type === 'steel') {
        return 'bgr-steel'
    } else if (type === 'rock') {
        return 'bgr-rock'
    } else if (type === 'fighting') {
        return 'bgr-fighting'
    } else if (type === 'poison') {
        return 'bgr-poison'
    } else if (type === 'bug') {
        return 'bgr-bug'
    }
    else if (type === 'ghost') {
        return 'bgr-ghost'
    }
    else if (type === 'fire') {
        return 'bgr-fire'
    }
    else if (type === 'water') {
        return 'bgr-water'
    }
    else if (type === 'electric') {
        return 'bgr-electric'
    }
    else if (type === 'psychic') {
        return 'bgr-psychic'
    }
    else if (type === 'ice') {
        return 'bgr-ice'
    }
    else if (type === 'dragon') {
        return 'bgr-dragon'
    }
    else if (type === 'dark') {
        return 'bgr-dark'
    }
    else if (type === 'fairy') {
        return 'bgr-fairy'
    }
    else if (type === 'flying') {
        return 'bgr-flying'
    }


}

export default colorTypes